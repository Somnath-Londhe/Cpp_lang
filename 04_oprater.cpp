#include <iostream>
using namespace std;
int main()
{
    int a = 10, b = 32;
    cout << "The value of a + b " << a + b << endl;
    cout << "The value of a - b " << a - b << endl;
    cout << "The value of a * b " << a * b << endl;
    cout << "The value of a / b " << a / b << endl;
    cout << "The value of a % b " << a % b << endl;
    cout << "The value of a ++ " << a++ << endl;
    cout << "The value of b ++ " << b++ << endl;
    cout << "The value of ++a " << ++a << endl;
    cout << "The value of --a " << --b << endl;
    // assimant opreter := "=",">=","<=","<",">";
    // comp oprater := "","","==","!="
    // logical oprater := "&&","||","!=";
    return 0;
}
